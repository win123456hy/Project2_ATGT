    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
           Users tk= new Users();
           if(txtUsername.getText().trim().equals("")){
            erormsg.setText("Username khong duoc de trong");
           }
           else{
           model.addRow(new Object[]{txtUsername.getText(), txtGender.getText(),txtEmail.getText(),txtPassword.getText(), txtCreatetime.getText()});
           }
    }//GEN-LAST:event_AddActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
                 if(Tabletaikhoan.getSelectedRow()==-1){  
         if(Tabletaikhoan.getRowCount()==0){
             erormsg.setText("Khong co du lieu");
         }
         else{
           erormsg.setText("Chon tai khoan can sua");
         }
             
         }
         else{
          model.setValueAt(txtUsername.getText(),Tabletaikhoan.getSelectedRow(),0);
          model.setValueAt(txtGender.getText(),Tabletaikhoan.getSelectedRow(),1);
          model.setValueAt(txtEmail.getText(),Tabletaikhoan.getSelectedRow(),2);  
          model.setValueAt(txtPassword.getText(),Tabletaikhoan.getSelectedRow(),3);
          model.setValueAt(txtCreatetime.getText(),Tabletaikhoan.getSelectedRow(),3);
         } 
    }//GEN-LAST:event_UpdateActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
         if(Tabletaikhoan.getSelectedRow()==-1){  
         if(Tabletaikhoan.getRowCount()==0){
             erormsg.setText("Khong co du lieu");
         }
         else{
           erormsg.setText("Chon tai khoan can xoa");
         }
             
         }
         else{
          model.removeRow(Tabletaikhoan.getSelectedRow());
         }
    }//GEN-LAST:event_DeleteActionPerformed

